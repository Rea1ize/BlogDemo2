package com.example.tonjies.view.util;

import android.util.Log;

public class L {
    //TAG
    public static String TAG = "tonjiesx";

    //5个等级 DIWE

    public static void d(String text) {
        Log.d(TAG, text + "");
    }

    public static void i(String text) {
        Log.i(TAG, text + "");
    }

    public static void w(String text) {
        Log.w(TAG, text + "");
    }

    public static void e(String text) {
        Log.e(TAG, text + "");
    }

}
